    public static int S_INT = 0;
    public static Object S_OBJ = null;

    public int testIntOps() {
        int a = 29;
        int b = 5;

        int rem = a % b;         // irem
        int neg = -a;            // ineg
        int sh = S_INT + 2;
        int shl = a << sh;       // ishl
        int shr = (-a) >> sh;    // ishr
        int ushr = (-a) >>> sh;  // iushr
        int andv = a & b;        // iand
        int orv = a | b;         // ior
        int xorv = a ^ b;        // ixor

        return rem + neg + shl + shr + ushr + andv + orv + xorv;
    }

    public int testCasts() {
        int x = 0x1234FF80;
        byte b = (byte) x;   // i2b
        char c = (char) x;   // i2c
        short s = (short) x; // i2s
        return b + c + s;
    }

    public int testStaticAndNull() {
        S_INT = 17;       // putstatic
        int r = S_INT;    // getstatic

        S_OBJ = null;     // putstatic
        Object a = S_OBJ; // getstatic
        if (a == null) {
            r = r + 1;    // ifnull / ifnonnull
        }

        S_OBJ = new Object();
        Object b = S_OBJ;
        if (b != null) {
            r = r + 2;
        }

        return r;
    }

